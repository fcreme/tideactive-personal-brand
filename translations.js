// Función para cambiar el idioma
function changeLanguage(lang) {
    // Guardar el idioma seleccionado en localStorage
    localStorage.setItem('selectedLanguage', lang);
    
    // Actualizar el atributo lang del HTML
    document.documentElement.lang = lang;
    
    // Traducir todos los elementos con atributo data-translate
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });
    
    // Actualizar el texto del botón de cambio de idioma
    const langButton = document.getElementById('language-switch');
    if (langButton) {
        langButton.textContent = translations[lang]['language.switch'];
    }
}

// Función para inicializar el idioma
function initLanguage() {
    // Obtener el idioma guardado o usar español por defecto
    const savedLang = localStorage.getItem('selectedLanguage') || 'es';
    changeLanguage(savedLang);
}

// Exportar funciones para uso global
window.translations = translations;
window.changeLanguage = changeLanguage;
window.initLanguage = initLanguage;
